let currentLanguage: 'en' | 'ru' = 'en';

export const setLanguage = (lang: 'en' | 'ru') => {
    currentLanguage = lang;
};

export const t = (key: keyof typeof translations.en, params?: { [key: string]: string | number | undefined }): string => {
    let str = translations[currentLanguage]?.[key] || translations.en[key] || key;
    if (params) {
        Object.keys(params).forEach(pKey => {
            const value = params[pKey];
            if (value !== undefined) {
                str = str.replace(`{{${pKey}}}`, String(value));
            }
        });
    }
    return str;
};